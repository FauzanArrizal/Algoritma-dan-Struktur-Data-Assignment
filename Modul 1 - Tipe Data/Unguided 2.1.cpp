#include <iostream>
using namespace std;

class Person {
public:
    const char *name;
    int age;
};

int main() {
    // Membuat objek dari class Person
    Person person;
    person.name = "Ojan";
    person.age = 19;

    // Menampilkan informasi person
    cout << "Nama: " << person.name << ", Usia: " << person.age << endl;

    return 0;
}
